<?php 
$n = 8;
for ($i=1; $i < $n; $i++) { 
    for ($j=1; $j < $i; $j++) { 
        echo $j ." ";

    }
    echo"\n";
}



$num = 1;
for ($i=1; $i <= 8; $i++) { 
    for ($j=1; $j < $i; $j++) { 
        echo $num ." ";
        $num++;
    }
    echo"\n";
}


for ($i=1; $i <= 8; $i++) { 
    for ($j=1; $j < $i; $j++) { 
        echo ($i * $j). "";

    }
    echo"\n";
}
?>